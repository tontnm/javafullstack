package javaoop.oop03;

public class Shit {
    private String mauSac;
    private double trongLuong;

    public Shit(String mauSac, double trongLuong) {
        this.mauSac = mauSac;
        this.trongLuong = trongLuong;
    }

    public String getMauSac() {
        return mauSac;
    }

    public double getTrongLuong() {
        return trongLuong;
    }
}
